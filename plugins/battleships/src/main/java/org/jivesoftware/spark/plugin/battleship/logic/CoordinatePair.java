package org.jivesoftware.spark.plugin.battleship.logic;

public class CoordinatePair {
    private final int x, y;

    public CoordinatePair(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
