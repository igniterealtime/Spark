        private ColorModel colorModel = null;
        private Rectangle rect = null;
        private Point offset = null;
        private Dimension size = null;
        private int[] gradientSample = null;
        private Hashtable rasterCache = new Hashtable();

        public GradientPaintContext(ColorModel cm, Rectangle r, Point o, Dimension s) {
            colorModel = cm;
            rect = r;
            offset = o;
            size = s;
            createSample();
        }

        public void dispose() {
//            rasterCache.clear();
        }

        public ColorModel getColorModel() {
            return colorModel;
        }

        public Raster getRaster(int x, int y, int w, int h) {
            // if gradientSample is not created (maybe size is invalid) return a solid lightgray raster. 
            // normally this should not happen
            if (gradientSample == null) {
                // create a compatible raster
                WritableRaster raster = colorModel.createCompatibleWritableRaster(w, h);
                int color = (220 << 16) | (220 << 8) | (220);
                // simply fill the raster with a color value of RGB(220,220,220)
                for (int j = 0; j < h; j++) {
                    for (int i = 0; i < w; i++) {
                        raster.setDataElements(i, j, colorModel.getDataElements(color, null));
                    }
                }
                return raster;
            } else {
                // if the x position is earlier requested we can return the chached raster 
                Object o = rasterCache.get(new Integer(x));
                if (o != null) {
                    return (Raster) o;
                } else {
                    // create a compatible raster
                    WritableRaster raster = colorModel.createCompatibleWritableRaster(w, h);
                    // calculate the offset into the gradientSample
                    int offs = x - (int) rect.x + offset.x;
                    // set the pixels of the raster as a combination of PATTERN pixels and gradientSample pixels
                    for (int j = 0; j < h; j++) {
                        for (int i = 0; i < w; i++) {
                            // calculate the coords of the PATTERN pixel
                            int xi = Math.abs(offset.x + x + i) % PATTERN_WIDTH;
                            int yi = Math.abs(offset.y + y + j) % PATTERN_HEIGHT;
                            int c1 = gradientSample[Math.abs(offs + i) % gradientSample.length];
                            // calculate the coord of the gradientSample pixel
                            int c2 = PATTERN[(yi * PATTERN_WIDTH) + xi];
                            // combine the pixels
                            int c = c1 - c2;
                            // calculate the color value 
                            int color = ((c & 0xFF) << 16) | ((c & 0xFF) << 8) | (c & 0xFF);
                            // put it into the raster
                            raster.setDataElements(i, j, colorModel.getDataElements(color, null));
                        }
                    }
                    // remember this raster 
                    rasterCache.put(new Integer(x), raster);
                    return raster;
                }
            }
        }

        private void createSample() {
            // do not create a gradientSample if size does not match
            if ((size.width > 8) && (size.width < 4096)) {
                // we only need to calculate the first part of gradientSample the second one is the inverse copy
                int w = (size.width + 1) / 2;
                gradientSample = new int[w * 2];

                // create the gradientSample from light gray RGB(224,224,224) to white RGB(255,255,255)
                float c1 = 224.0f;
                float c2 = 255.0f;
                float dc = (c2 - c1) / w;
                float c = c1;
                gradientSample[0] = (int) c1;
                for (int i = 1; i < w - 1; i++) {
                    c += dc;
                    gradientSample[i] = (int) c;
                }
                gradientSample[w - 1] = (int) c2;

                // copy the first part
                for (int i = w, j = w - 1; i < size.width; i++, j--) {
                    gradientSample[i] = gradientSample[j];
                }
            }
        }
